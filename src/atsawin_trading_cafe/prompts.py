def build_messages(user_text: str, extra_context: str | None = None) -> list[dict[str, str]]:
    """Build chat messages for an LLM provider."""
    content = user_text if not extra_context else f"บริบทเพิ่มเติม:\n{extra_context}\n\nคำถาม/คำสั่ง:\n{user_text}"
    return [
        {"role": "system", "content": ATSAWIN_TRADING_CAFE_SYSTEM_PROMPT},
        {"role": "user", "content": content},
    ]
